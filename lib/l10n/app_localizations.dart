  static final ValueNotifier<Locale> localeNotifier = ValueNotifier(const Locale('es'));

  static Future<void> init() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final langCode = prefs.getString('languageCode');
      if (langCode != null) {
        localeNotifier.value = Locale(langCode);
      }
    } catch (e) {
      debugPrint("Error loading language: $e");
    }
  }

  static Future<void> changeLanguage(Locale locale) async {
    try {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString('languageCode', locale.languageCode);
      localeNotifier.value = locale;
    } catch (e) {
      debugPrint("Error saving language: $e");
    }
  }

  static String get(String key) {
    final lang = localeNotifier.value.languageCode;
    return _localizedValues[lang]?[key] ?? _localizedValues['es']?[key] ?? key;
  }

